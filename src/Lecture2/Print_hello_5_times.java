package Lecture2;

import java.util.Scanner;

public class Print_hello_5_times {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int n = sc.nextInt();

        int i = 1;

        while(i<=n){
            System.out.println("one punch man");
            i++;
        }
    }
}
